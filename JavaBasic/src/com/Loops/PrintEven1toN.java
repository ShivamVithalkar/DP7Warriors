package com.Loops;
import java.util.Scanner;

public class PrintEven1toN 
{
	public static void main(String[] args) 
	{
		Scanner sc =new Scanner(System.in);
		
		System.out.println("Enter the N number:");
		int n=sc.nextInt();
		
		if(n%2==0)
		{
			for(int i=n;i<=n;i++)
			{
				System.out.println(n);
			}
		}

	}

}
